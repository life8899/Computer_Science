public class Array
{
    public static void main(String[] args)
    {
        String[] names = {"Kyle", "Michael", "Allison", "Alex"};
        System.out.println(names[1]);
        System.out.println(names[3]);
        for (int i = 0; i <4; i++)
        {
            System.out.println(names[i]);
        }
    }
}
